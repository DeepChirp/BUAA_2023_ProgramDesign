#include <stdio.h>

int main()
{
    int n;
    scanf("%d", &n);
    switch (n)
    {
    case 1:
        printf("Life cannot possibly bear splendid flowers from lies.\n");
        break;
    case 2:
        printf("Where is there dignity unless there is honesty?\n");
        break;
    case 3:
        printf("If one loses honesty, he(she) will lose everything.\n");
        break;

    default:
        break;
    }

    printf("I promise I do not cheat in the exam.");

    return 0;
}
